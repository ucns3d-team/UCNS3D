SUBROUTINE COMPUTE_GRADIENTS_INNER_turb_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI, IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, U_CT_L)!check_all
!> @brief
!> This subroutine computes the gradients of the turbulence variables of each interior cell using the Green-Gauss algorithm
IMPLICIT NONE
#ifdef LEASTSQUARES_GPU_KERNEL
!$omp declare target
#endif
INTEGER,INTENT(IN)::N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI
TYPE(A_ELEMENT_NUMBER),ALLOCATABLE,DIMENSION(:,:),INTENT(INOUT)::IELEM_L
TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON3_L
TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON5_L
TYPE(A_U_CENTRE),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::U_C_L, U_CT_L
REAL,DIMENSION(turbulenceequations+passivescalar)::SOLS1,SOLS2
REAL,DIMENSION(turbulenceequations+passivescalar,nof_Variables)::SOLS_F
REAL,DIMENSION(dimensiona)::NORMAL_ALL
REAL::OOV2,angle1,angle2
INTEGER::I,J,K,L,var2
real,dimension(1:nof_Variables)::leftv







I=ICONSIDERED
SOLS_F=zero
OOV2=1.0D0/IELEM_L(N,I)%TOTVOLUME


if (dimensiona.eq.3)then


	  SOLS1(1:turbulenceequations+passivescalar)=U_CT_L(I)%VAL(1,1:turbulenceequations+passivescalar)/U_C_L(I)%VAL(1,1)

DO J=1,IELEM_L(N,I)%IFCA
			ANGLE1=IELEM_L(N,I)%FACEANGLEX(J)
			ANGLE2=IELEM_L(N,I)%FACEANGLEY(J)
				NORMAL_ALL(1)=(COS(ANGLE1)*SIN(ANGLE2))
				NORMAL_ALL(2)=(SIN(ANGLE1)*SIN(ANGLE2))
				NORMAL_ALL(3)=(COS(ANGLE2))

			SOLS2(1:turbulenceequations+passivescalar)=U_CT_L(IELEM_L(N,I)%INEIGH(J))%VAL(1,1:turbulenceequations+passivescalar)/U_C_L(IELEM_L(N,I)%INEIGH(J))%VAL(1,1)


			DO K=1,3
			SOLS_F(1:turbulenceequations+passivescalar,K)=SOLS_F(1:turbulenceequations+passivescalar,K)+((OO2*(SOLS2(1:turbulenceequations+passivescalar)+SOLS1(1:turbulenceequations+passivescalar)))*NORMAL_ALL(K)*IELEM_L(N,I)%SURF(J)*OOV2)

			END DO
END DO

			  DO VAR2=1,turbulenceequations+passivescalar
			    ILOCAL_RECON5_L(ICONSIDERED)%GRADIENTSTURB(1,1:3,VAR2)=SOLs_f(var2,1:3)
			    ILOCAL_RECON3_L(I)%GRADs(4+var2,1:3)=SOLs_f(var2,1:3)
			 END DO

else


 SOLS1(1:turbulenceequations+passivescalar)=U_CT_L(I)%VAL(1,1:turbulenceequations+passivescalar)/U_C_L(I)%VAL(1,1)

DO J=1,IELEM_L(N,I)%IFCA
			ANGLE1=IELEM_L(N,I)%FACEANGLEX(J)
			ANGLE2=IELEM_L(N,I)%FACEANGLEY(J)
				NORMAL_ALL(1)=angle1
				NORMAL_ALL(2)=angle2

			SOLS2(1:turbulenceequations+passivescalar)=U_CT_L(IELEM_L(N,I)%INEIGH(J))%VAL(1,1:turbulenceequations+passivescalar)/U_C_L(IELEM_L(N,I)%INEIGH(J))%VAL(1,1)


			DO K=1,2
			SOLS_F(1:turbulenceequations+passivescalar,K)=SOLS_F(1:turbulenceequations+passivescalar,K)+((OO2*(SOLS2(1:turbulenceequations+passivescalar)+SOLS1(1:turbulenceequations+passivescalar)))*NORMAL_ALL(K)*IELEM_L(N,I)%SURF(J)*OOV2)

			END DO
END DO

			  DO VAR2=1,turbulenceequations+passivescalar
			    ILOCAL_RECON5_L(ICONSIDERED)%GRADIENTSTURB(1,1:2,VAR2)=SOLs_f(var2,1:2)
			    ILOCAL_RECON3_L(I)%GRADs(3+var2,1:2)=SOLs_f(var2,1:2)
			 END DO







end if




end subroutine COMPUTE_GRADIENTS_INNER_turb_GGS_VISCOUS




