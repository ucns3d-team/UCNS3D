SUBROUTINE COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)!check all
!> @brief
!> This subroutine computes the gradients of the conserved variables of each cell using the least-squares
   IMPLICIT NONE
#ifdef LEASTSQUARES_GPU_KERNEL
	!$omp declare target
#endif
   INTEGER,INTENT(IN)::N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI
   TYPE(A_ELEMENT_NUMBER),ALLOCATABLE,DIMENSION(:,:),INTENT(INOUT)::IELEM_L
   TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON3_L
   TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON5_L
   TYPE(A_U_CENTRE),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::U_C_L
   TYPE(A_EXCHANGE_SOLHI),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::IEXSOLHIR_L
   REAL,DIMENSION(nof_variables)::SOLS1
   REAL,DIMENSION(nof_variables,typesten)::SOLS2
   REAL,DIMENSION(NUMBER_OF_NEI-1,nof_variables,IELEM_L(N,ICONSIDERED)%ADMIS)::MATRIX_1
   REAL,DIMENSION(NUMBER_OF_DOG,NOF_VARIABLES,IELEM_L(N,ICONSIDERED)%ADMIS)::MATRIX_2
   REAL,DIMENSION(NUMBER_OF_DOG,NOF_VARIABLES,IELEM_L(N,ICONSIDERED)%ADMIS)::SOL_M	
   REAL,DIMENSION(1:NOF_VARIABLES)::LEFTV,RIGHTV
   REAL::MP_PINFl,GAMMAL
   INTEGER::I,VAR2,iq,ll,IMAX
   REAL::TEMPXX
	 I=ICONSIDERED

   !ALLOCATE(MATRIX_1(NUMBER_OF_NEI-1,nof_variables,IELEM_L(N,ICONSIDERED)%ADMIS))	
   !ALLOCATE(MATRIX_2(NUMBER_OF_DOG,NOF_VARIABLES,IELEM_L(N,ICONSIDERED)%ADMIS))	
   !ALLOCATE	(SOL_M(NUMBER_OF_DOG,NOF_VARIABLES,IELEM_L(N,ICONSIDERED)%ADMIS))	

   
   IMAX=NUMBER_OF_NEI-1


   I=ICONSIDERED
   SOLS1=ZERO; SOLS2=ZERO; matrix_2=zero; sol_m=zero; MATRIX_1=ZERO;MATRIX_2=ZERO


   SOLS1(1:nof_variables)=U_C_L(ILOCAL_RECON3_L(I)%IHEXL(1,1))%VAL(1,1:nof_variables)
   IF (WENWRT.EQ.3)THEN
   LEFTV(1:NOF_VARIABLES)=SOLS1(1:nof_variables)
   CALL CONS2PRIM(N,leftv,MP_PINFl,gammal)
   SOLS1(1:NOF_VARIABLES)=LEFTV(1:NOF_VARIABLES)
   END IF



   if (ILOCAL_RECON3_L(I)%LOCAL.eq.1)then

      DO LL=1,IELEM_L(N,I)%ADMIS;


        if ((ees.ne.5).or.(ll.eq.1))then
             DO IQ=1,imax
            SOLS2(1:nof_variables,ll)=U_C_L(ILOCAL_RECON3_L(I)%IHEXL(LL,IQ+1))%VAL(1,1:nof_variables)


            IF (WENWRT.EQ.3)THEN
            LEFTV(1:NOF_VARIABLES)=SOLS2(1:nof_variables,LL)
            CALL CONS2PRIM(N,leftv,MP_PINFl,gammal)
            SOLS2(1:NOF_VARIABLES,LL)=LEFTV(1:NOF_VARIABLES)
            END IF

            IF(PER_ROT.EQ.1)THEN
                IF (ILOCAL_RECON3_L(I)%PERIODICFLAG(LL,IQ+1).EQ.2) THEN
                    tempxx=sols2(2,LL)
                    sols2(2,LL)=tempxx*cos(angle_per)-sols2(3,LL)*sin(angle_per)
                    sols2(3,LL)=tempxx*sin(angle_per)+sols2(3,LL)*cos(angle_per)
                end if
                IF (ILOCAL_RECON3_L(I)%PERIODICFLAG(LL,IQ+1).EQ.1) THEN
                    tempxx=sols2(2,LL)
                    sols2(2,LL)=tempxx*cos(-angle_per)-sols2(3,LL)*sin(-angle_per)
                    sols2(3,LL)=tempxx*sin(-angle_per)+sols2(3,LL)*cos(-angle_per)
                END IF
            END IF

            MATRIX_1(IQ,1:nof_variables,ll)=(SOLS2(1:nof_variables,ll)-SOLS1(1:nof_variables))

            END DO

        eLSE

               DO IQ=1,numneighbours2-1
            SOLS2(1:nof_variables,ll)=U_C_L(ILOCAL_RECON3_L(I)%IHEXLC(LL,IQ+1))%VAL(1,1:nof_variables)
            IF (WENWRT.EQ.3)THEN
            LEFTV(1:NOF_VARIABLES)=SOLS2(1:nof_variables,LL)
            CALL CONS2PRIM(N,leftv,MP_PINFl,gammal)
            SOLS2(1:NOF_VARIABLES,LL)=LEFTV(1:NOF_VARIABLES)
            END IF
            MATRIX_1(IQ,1:nof_variables,ll)=(SOLS2(1:nof_variables,ll)-SOLS1(1:nof_variables))


         END DO

        END IF




    end do
     DO LL=1,IELEM_L(N,I)%ADMIS;
        if ((ees.ne.5).or.(ll.eq.1))then

!          CALL DGEMM('N','N',IELEM_L(N,I)%IDEGFREE,nof_variables,imax,&
!          ALPHA,ILOCAL_RECON3_L(I)%invmat_stencilt(1:IELEM_L(N,I)%IDEGFREE,1:imax,LL),&
!          IELEM_L(N,I)%IDEGFREE,MATRIX_1(1:imax,1:nof_variables,ll),&
! imax,BETA,SOL_M(1:IELEM_L(N,I)%IDEGFREE,1:nof_variables,ll),IELEM_L(N,I)%IDEGFREE)


		SOL_M(1:IELEM_L(N,I)%IDEGFREE,1:nof_variables,ll)=MATMUL(ILOCAL_RECON3_L(I)%invmat_stencilt(1:IELEM_L(N,I)%IDEGFREE,1:imax,LL),MATRIX_1(1:imax,1:nof_variables,ll))




         ELSE
!          CALL DGEMM('N','N',IDEGFREE2,nof_variables,numneighbours2-1,&
!          ALPHA,ILOCAL_RECON3_L(I)%invmat_stenciltC(1:IDEGFREE2,1:numneighbours2-1,LL),&
!          IDEGFREE2,MATRIX_1(1:numneighbours2-1,1:nof_variables,ll),&
! numneighbours2-1,BETA,SOL_M(1:IDEGFREE2,1:nof_variables,ll),IDEGFREE2)

		SOL_M(1:IDEGFREE2,1:nof_variables,ll)=MATMUL(ILOCAL_RECON3_L(I)%invmat_stenciltC(1:IDEGFREE2,1:numneighbours2-1,LL),MATRIX_1(1:numneighbours2-1,1:nof_variables,ll))


         END IF
      end do


       DO LL=1,IELEM_L(N,I)%ADMIS;
       if ((ees.ne.5).or.(ll.eq.1))then
      ILOCAL_RECON5_L(ICONSIDERED)%GRADIENTS(LL,1:NUMBER_OF_DOG,1:nof_variables)=SOL_M(1:NUMBER_OF_DOG,1:nof_variables,ll)
      ELSE
      ILOCAL_RECON5_L(ICONSIDERED)%GRADIENTSC(LL,1:IDEGFREE2,1:nof_variables)=SOL_M(1:IDEGFREE2,1:nof_variables,ll)
      END IF
      end do

   else


      DO LL=1,IELEM_L(N,I)%ADMIS;
        if ((ees.ne.5).or.(ll.eq.1))then

                DO IQ=1,imax


                    IF (ILOCAL_RECON3_L(I)%IHEXB(LL,IQ+1).EQ.N)THEN

                    SOLS2(1:nof_variables,ll)=U_C_L(ILOCAL_RECON3_L(I)%IHEXL(LL,IQ+1))%VAL(1,1:nof_variables)
                    else
                    SOLS2(1:nof_variables,ll)=IEXSOLHIR_L(ILOCAL_RECON3_L(I)%IHEXN(LL,IQ+1))%SOL(ILOCAL_RECON3_L(I)%IHEXL(LL,IQ+1),1:nof_variables)
                    end if

                    IF (WENWRT.EQ.3)THEN
            LEFTV(1:NOF_VARIABLES)=SOLS2(1:nof_variables,LL)
            CALL CONS2PRIM(N,leftv,MP_PINFl,gammal)
            SOLS2(1:NOF_VARIABLES,LL)=LEFTV(1:NOF_VARIABLES)
            END IF
                    IF(PER_ROT.EQ.1)THEN
                        IF (ILOCAL_RECON3_L(I)%PERIODICFLAG(LL,IQ+1).EQ.1) THEN
!             write(2900+n,*),IELEM_L(N,I)%XXC,IELEM_L(N,I)%YYC,IELEM_L(N,I)%ZZC
                            if (IELEM_L(N,I)%XXC.gt.0.0d0) then
                                tempxx=sols2(2,LL)
                                sols2(2,LL)=sols2(3,LL)
                                sols2(3,LL)=-TEMPXX
                            end if
                            if (IELEM_L(N,I)%XXC.lt.0.0d0) then
                                tempxx=sols2(2,LL)
                                sols2(2,LL)=-sols2(3,LL)
                                sols2(3,LL)=TEMPXX
                            end if
                        END IF
                    END IF

                    MATRIX_1(IQ,1:nof_variables,ll)=(SOLS2(1:nof_variables,ll)-SOLS1(1:nof_variables))

                END DO





         ELSE
                    DO IQ=1,numneighbours2-1

                    IF (ILOCAL_RECON3_L(I)%IHEXBC(LL,IQ+1).EQ.N)THEN

                    SOLS2(1:nof_variables,ll)=U_C_L(ILOCAL_RECON3_L(I)%IHEXLC(LL,IQ+1))%VAL(1,1:nof_variables)
                    else
                    SOLS2(1:nof_variables,ll)=IEXSOLHIR_L(ILOCAL_RECON3_L(I)%IHEXNC(LL,IQ+1))%SOL(ILOCAL_RECON3_L(I)%IHEXLC(LL,IQ+1),1:nof_variables)
                    end if
                                IF (WENWRT.EQ.3)THEN
                        LEFTV(1:NOF_VARIABLES)=SOLS2(1:nof_variables,LL)
                        CALL CONS2PRIM(N,leftv,MP_PINFl,gammal)
                        SOLS2(1:NOF_VARIABLES,LL)=LEFTV(1:NOF_VARIABLES)
                        END IF

                    MATRIX_1(IQ,1:nof_variables,ll)=(SOLS2(1:nof_variables,ll)-SOLS1(1:nof_variables))

                END DO




         END IF


        end do

         DO LL=1,IELEM_L(N,I)%ADMIS;
        if ((ees.ne.5).or.(ll.eq.1))then


!          CALL DGEMM('N','N',IELEM_L(N,I)%IDEGFREE,nof_variables,imax,&
!          ALPHA,ILOCAL_RECON3_L(I)%invmat_stencilt(1:IELEM_L(N,I)%IDEGFREE,1:imax,LL),&
!          IELEM_L(N,I)%IDEGFREE,MATRIX_1(1:imax,1:nof_variables,ll),&
! imax,BETA,SOL_M(1:IELEM_L(N,I)%IDEGFREE,1:nof_variables,ll),IELEM_L(N,I)%IDEGFREE)


		SOL_M(1:IELEM_L(N,I)%IDEGFREE,1:nof_variables,ll)=MATMUL(ILOCAL_RECON3_L(I)%invmat_stencilt(1:IELEM_L(N,I)%IDEGFREE,1:imax,LL),MATRIX_1(1:imax,1:nof_variables,ll))

         ELSE


!          CALL DGEMM('N','N',IDEGFREE2,nof_variables,numneighbours2-1,&
!          ALPHA,ILOCAL_RECON3_L(I)%invmat_stenciltC(1:IDEGFREE2,1:numneighbours2-1,LL),&
!          IDEGFREE2,MATRIX_1(1:numneighbours2-1,1:nof_variables,ll),&
! numneighbours2-1,BETA,SOL_M(1:IDEGFREE2,1:nof_variables,ll),IDEGFREE2)

		SOL_M(1:IDEGFREE2,1:nof_variables,ll)=MATMUL(ILOCAL_RECON3_L(I)%invmat_stenciltC(1:IDEGFREE2,1:numneighbours2-1,LL),MATRIX_1(1:numneighbours2-1,1:nof_variables,ll))



         END IF
      end do


       DO LL=1,IELEM_L(N,I)%ADMIS;
       if ((ees.ne.5).or.(ll.eq.1))then
