SUBROUTINE COMPUTE_GRADIENTS_INNER_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L,ILOCAL_RECON3_L,U_C_L)!check_all
!> @brief
!> This subroutine computes the gradients of the primitive variables of each interior cell using the Green-Gauss algorithm
IMPLICIT NONE
#ifdef LEASTSQUARES_GPU_KERNEL
!$omp declare target
#endif
INTEGER,INTENT(IN)::N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI
TYPE(A_ELEMENT_NUMBER),ALLOCATABLE,DIMENSION(:,:),INTENT(INOUT)::IELEM_L
TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON3_L
TYPE(A_U_CENTRE),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::U_C_L
REAL,DIMENSION(1:nof_variables)::SOLS1,SOLS2,dudl,aver1
REAL,DIMENSION(1:nof_variables,dimensiona)::SOLS_F
REAL,DIMENSION(3)::NORMAL_ALL
REAL::OOV2,titj,MP_PINFl,gammal,angle1,angle2
INTEGER::I,J,K,L
real,dimension(1:nof_Variables)::leftv


I=ICONSIDERED
SOLS_F=zero
OOV2=1.0D0/IELEM_L(N,I)%TOTVOLUME


if (dimensiona.eq.3)then


	    leftv(1:nof_variables)=U_C_L(I)%VAL(1,1:nof_variables)
	    call CONS2PRIM(N,leftv,MP_PINFl,gammal)
	  SOLS1(1:nof_variables)=leftv(1:nof_variables)
	  sols1(5)=leftv(5)/(leftv(1)*R_gas)

DO J=1,IELEM_L(N,I)%IFCA
			ANGLE1=IELEM_L(N,I)%FACEANGLEX(J)
			ANGLE2=IELEM_L(N,I)%FACEANGLEY(J)
				NORMAL_ALL(1)=(COS(ANGLE1)*SIN(ANGLE2))
				NORMAL_ALL(2)=(SIN(ANGLE1)*SIN(ANGLE2))
				NORMAL_ALL(3)=(COS(ANGLE2))

			leftv(1:nof_variables)=U_C_L(IELEM_L(N,I)%INEIGH(J))%VAL(1,1:nof_variables)
			call CONS2PRIM(N,leftv,MP_PINFl,gammal)
			SOLS2(1:nof_variables)=leftv(1:nof_variables)
			sols2(5)=leftv(5)/(leftv(1)*R_gas)
 			DO K=1,3
 			SOLS_F(1:nof_variables,K)=SOLS_F(1:nof_variables,K)+((OO2*(SOLS2(1:nof_variables)+SOLS1(1:nof_variables)))*NORMAL_ALL(K)*IELEM_L(N,I)%SURF(J)*OOV2)
 			END DO
END DO

			DO K=1,3
			ILOCAL_RECON3_L(I)%GRADs(1:3,k)=sOLS_F(2:4,K)
			ILOCAL_RECON3_L(I)%GRADs(4,k)=sOLS_F(5,K)
			END DO

eLSE

