SUBROUTINE ALLGRADS_INNER(N,ICONSIDERED,IELEM_L,ILOCAL_RECON3_L,ILOCAL_RECON5_L,U_C_L,U_CT_L,IEXSOLHIR_L)
!> @brief
!> This subroutine calls the gradient approximation subroutines for every interior cell
IMPLICIT NONE
#ifdef LEASTSQUARES_GPU_KERNEL
!$omp declare target
#endif
INTEGER,INTENT(IN)::N,ICONSIDERED
TYPE(A_ELEMENT_NUMBER),ALLOCATABLE,DIMENSION(:,:),INTENT(INOUT)::IELEM_L
TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON3_L
TYPE(A_LOCAL_RECON3),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::ILOCAL_RECON5_L
TYPE(A_U_CENTRE),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::U_C_L, U_CT_L
TYPE(A_EXCHANGE_SOLHI),ALLOCATABLE,DIMENSION(:),INTENT(INOUT)::IEXSOLHIR_L
INTEGER::I
INTEGER::NUMBER_OF_DOG,NUMBER_OF_NEI,IMAX
I=iconsidered
NUMBER_OF_DOG=IELEM_L(N,I)%IDEGFREE
NUMBER_OF_NEI=IELEM_L(N,I)%inumneighbours
! imax=NUMBER_OF_NEI-1



IF (DG.EQ.1)THEN

CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)


ELSE



SELECT CASE(IELEM_L(N,I)%GGS)

CASE(0)	!LEAST SQUARES EVERYTHING
    SELECT case (ITESTCASE)
 
    CASE(1,2,3)
      
    CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)


    IF (INITCOND.EQ.95)THEN
    CALL COMPUTE_GRADIENTS_INNER_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L,ILOCAL_RECON3_L,U_C_L)
    END IF

      
    CASE(4)
	CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)
    CALL COMPUTE_GRADIENTS_INNER_MEAN_LSQ_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI, IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)
		IF (TURBULENCE.EQ.1)THEN
			CALL COMPUTE_GRADIENTS_INNER_turb_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI, IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, U_CT_L)
			CALL COMPUTE_GRADIENTS_TURB_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L,ILOCAL_RECON3_L,ILOCAL_RECON5_L,U_CT_L,IEXSOLHIR_L)
			CALL COMPUTE_GRADIENTS_TURB_LSQ_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L,ILOCAL_RECON3_L,ILOCAL_RECON5_L,U_C_L, U_CT_L,IEXSOLHIR_L)
		End if





   END SELECT
   
   
 CASE(1) !GREEN GAUSS EVERYTHING
      SELECT case (ITESTCASE)
 
 
       CASE(1,2,3)
      
      CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)


      IF (INITCOND.EQ.95)THEN
    CALL COMPUTE_GRADIENTS_INNER_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L,ILOCAL_RECON3_L,U_C_L)
    END IF



      CASE(4)
      CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)
      CALL COMPUTE_GRADIENTS_INNER_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L,ILOCAL_RECON3_L,U_C_L)
      
      
      IF (TURBULENCE.EQ.1)THEN
      CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, IEXSOLHIR_L)
      CALL COMPUTE_GRADIENTS_INNER_turb_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI, IELEM_L, ILOCAL_RECON3_L, ILOCAL_RECON5_L, U_C_L, U_CT_L)
      END IF
      



      END SELECT
      

 END SELECT


 END IF



END SUBROUTINE ALLGRADS_INNER

SUBROUTINE ALLGRADS_MIX(N,ICONSIDERED)
!> @brief
!> This subroutine calls the gradient approximation subroutines for every non-interior cell
IMPLICIT NONE
INTEGER,INTENT(IN)::N,ICONSIDERED
INTEGER::I
INTEGER::NUMBER_OF_DOG,NUMBER_OF_NEI,IMAX
I=iconsidered
NUMBER_OF_DOG=IELEM(N,I)%IDEGFREE
NUMBER_OF_NEI=IELEM(N,I)%inumneighbours


IF (DG.EQ.1)THEN

		CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM, ILOCAL_RECON3, ILOCAL_RECON5, U_C, IEXSOLHIR)


ELSE







			IF (FASTEST.ne.1)THEN !LEAST SQUARES
			SELECT CASE(IELEM(N,I)%GGS)

				CASE(0)	!LEAST SQUARES EVERYTHING
				SELECT case (ITESTCASE)





				CASE(1,2,3)

				CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM, ILOCAL_RECON3, ILOCAL_RECON5, U_C, IEXSOLHIR)

				IF (INITCOND.EQ.95)THEN
				CALL COMPUTE_GRADIENTS_MIX_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)
				END IF




				CASE(4)
				CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM, ILOCAL_RECON3, ILOCAL_RECON5, U_C, IEXSOLHIR)

								IF (ielem(n,i)%walls.NE.1)THEN
									CALL COMPUTE_GRADIENTS_INNER_MEAN_LSQ_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM, ILOCAL_RECON3, ILOCAL_RECON5, U_C, IEXSOLHIR)
								ELSE
									CALL COMPUTE_GRADIENTS_wall_mean_LSQ_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)
								END IF

								IF (TURBULENCE.EQ.1)THEN
									CALL COMPUTE_GRADIENTS_MIX_turb_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)
									CALL COMPUTE_GRADIENTS_TURB_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM,ILOCAL_RECON3,ILOCAL_RECON5,U_CT,IEXSOLHIR)

									IF (ielem(n,i)%walls.NE.1)THEN
										CALL COMPUTE_GRADIENTS_TURB_LSQ_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM,ILOCAL_RECON3,ILOCAL_RECON5,U_C, U_CT,IEXSOLHIR)
									ELSE
										CALL COMPUTE_GRADIENTS_wall_turb_LSQ_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)
									END IF
								END IF







			END SELECT


			CASE(1)
				SELECT case (ITESTCASE)


				CASE(1,2,3)

				CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM, ILOCAL_RECON3, ILOCAL_RECON5, U_C, IEXSOLHIR)


				IF (INITCOND.EQ.95)THEN
				CALL COMPUTE_GRADIENTS_MIX_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)
				END IF

				CASE(4)
				CALL COMPUTE_GRADIENTS_MEAN_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM, ILOCAL_RECON3, ILOCAL_RECON5, U_C, IEXSOLHIR)
				CALL COMPUTE_GRADIENTS_MIX_MEAN_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)


				IF (TURBULENCE.EQ.1)THEN
				CALL COMPUTE_GRADIENTS_TURB_LSQ(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI,IELEM,ILOCAL_RECON3,ILOCAL_RECON5,U_CT,IEXSOLHIR)

				CALL COMPUTE_GRADIENTS_MIX_turb_GGS_VISCOUS(N,ICONSIDERED,NUMBER_OF_DOG,NUMBER_OF_NEI)
				END IF


				END SELECT
			END SELECT


			END IF

	end if
      


END SUBROUTINE ALLGRADS_MIX




