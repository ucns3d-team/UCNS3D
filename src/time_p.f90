    end if
    
  END DO
  !$OMP END DO
  else
  !$OMP DO
  DO I=1,KMAXE
    U_C(I)%val(ind1,:)=ZERO
    IF ((TURBULENCE.EQ.1).OR.(PASSIVESCALAR.GT.0))THEN
    
    
